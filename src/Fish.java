public class Fish {
    private int rudderDirection;
    private int speed;

    public int getRudderDirection() {
        return rudderDirection;
    }

    public int getSpeed() {
        return speed;
    } 

    public void setRudderDirection(int direction) {
        rudderDirection = direction;
    } 

    public void setSpeed(int speed) {
        this.speed = speed;
    } 
}

